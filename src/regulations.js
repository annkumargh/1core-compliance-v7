// ── Formatted reg object for UI consumption ───────────────────────────────────
export function getRegForUI(state) {
  const r = STATE_REGS[state] || STATE_REGS['TX'];
  const ru = r.rules || {};
  return {
    agency:      r.agency      || 'State Childcare Licensing',
    agencyPhone: r.agencyPhone || '',
    citation:    r.citation    || state,
    qrisName:    r.qrs         || 'Quality Rating System',
    refs:        r.refs        || {},   // statutory section references (CA, DC, TX, FL, NJ, VA)
    rules: {
      infantRatio:    `1:${r.infant}`,
      toddlerRatio:   `1:${r.toddler}`,
      preschoolRatio: `1:${r.preschool}`,
      schoolAgeRatio: `1:${r.schoolAge}`,
      indoorSqft:     r.indoorSqft,
      outdoorSqft:    r.outdoorSqft,
      trainingHours:  r.trainingHrs,
      inspPerYear:    r.inspPerYear,
      fencingMin:     parseInt(ru.minFencingHeight) || 4,
      hotWaterMax:    parseInt(ru.hotWaterMax) || 110,
      toiletRatio:    parseInt((ru.toiletRatio||'1:15').split(':')[1]) || 15,
      fireDrillFreq:  ru.fireDrillFreq    || 'Monthly',
      tornadoDrillFreq: ru.tornadoDrill   || 'Monthly',
      cprRenewal:     ru.cprRenewal       || 'Every 2 years',
      bgCheck:        ru.bgCheckType      || 'State + FBI',
      mandatedReporterRenewal: ru.mandatedReporterRenewal || 'Required',
      workforceRegistry: ru.workforceRegistry || 'Check state',
      qrisName:       r.qrs || '',
      recordRetention: ru.recordRetention || '2',
    }
  };
}
